package inventory;

public class Product {
	private String type;
	private int weight;
	private int price;

	public String getType() {
		return type;
	}

	public void SetType(String type) {
		this.type = type;
	}

	public int getWeight() {
		return weight;
	}

	public void setWeight(int weight) {
		this.weight = weight;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}
}
